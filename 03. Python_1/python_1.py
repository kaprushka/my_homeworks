print('Введите число a:')
a = int(input('> '))
print('Введите число b:')
b = int(input('> '))
print('Введите число c:')
c = int(input('> '))
if a + b == c:
    print('Числа a и b в сумме дают число c')
else:
    print('Числа a и b в сумме не дают число c')
if a / b == c:
    print('Число a при делении на число b дает число c')
else:
    print('Число a при делении на число b не дает число c')
